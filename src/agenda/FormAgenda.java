    private void jButtonExcluirNotificacaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirNotificacaoActionPerformed
        Compromisso compromisso = jListCompromissos.getSelectedValue();
        compromisso.notificacao.remove(jListNotificacoes.getSelectedIndex());
        
        ListModelNotificacao.clear();
        ListModelNotificacao.addAll(compromisso.getNotificacao());

        Listar();
        LimparCampos();
    }//GEN-LAST:event_jButtonExcluirNotificacaoActionPerformed

    private void Salvar(){
        try {
            // realiza a leitura dos valores da tela, e as conversões necessárias
            String auxTitulo = jTextFieldTitulo.getText();
            String auxDescricao = jTextAreaDescricao.getText();
            Date auxDataInicio = auxDateFormat.parse(jFormattedTextFieldDataInicio.getText());
            Date auxDataFim = auxDateFormat.parse(jFormattedTextFieldDataFim.getText());
            // cria o objeto do tipo compromisso
            var auxCompromisso = new Compromisso( auxTitulo, auxDescricao, auxDataInicio, auxDataFim );
            // adiciona no array agenda
            agenda.add( auxCompromisso );
        } catch (ParseException x) {
            JOptionPane.showMessageDialog(this, "Erro ao salvar compromisso!");
        }               
    }
    
    private void Listar(){
        ListModelCompromisso.clear();
        ListModelCompromisso.addAll(agenda);
        
        jListCompromissos.setModel(ListModelCompromisso);
        jListNotificacoes.setModel(ListModelNotificacao);  
    }
    
    private void LimparCampos(){
        
        jTextFieldTitulo.setText("");
        jTextAreaDescricao.setText("");
        jFormattedTextFieldDataInicio.setText("");
        jFormattedTextFieldDataFim.setText("");        
        
        jFormattedTextFieldTempo.setText("");
        jFormattedTextFieldUnidade.setText("");
        jFormattedTextFieldTipo.setText("");
    }
    
