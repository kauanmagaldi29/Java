    private void jButtonEditarCompromissoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditarCompromissoActionPerformed
            
        Compromisso compromisso = jListCompromissos.getSelectedValue();
        
        try{
            compromisso.setTitulo(jTextFieldTitulo.getText());
            compromisso.setDescricao(jTextAreaDescricao.getText());
            compromisso.setHoraInicio(auxDateFormat.parse(jFormattedTextFieldDataInicio.getText()));
            compromisso.setHoraFim(auxDateFormat.parse(jFormattedTextFieldDataFim.getText()));        
            
        }catch(Exception e)
        {
            JOptionPane.showMessageDialog(this, "Erro ao editar compromisso!");
        }

        Listar();
        LimparCampos();
    }//GEN-LAST:event_jButtonEditarCompromissoActionPerformed

    private void jButtonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirActionPerformed
        agenda.remove(jListCompromissos.getSelectedIndex());
        
        Listar();
        LimparCampos();
    }//GEN-LAST:event_jButtonExcluirActionPerformed

    private void SalvarNotificacaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SalvarNotificacaoActionPerformed
        Notificacao notificacao = new Notificacao();
        
        notificacao.setTempo(Byte.parseByte(jFormattedTextFieldTempo.getText()));
        notificacao.setTipo(jFormattedTextFieldUnidade.getText().charAt(0));
        notificacao.setUnidade(jFormattedTextFieldTipo.getText());   
        
        Compromisso compromisso = jListCompromissos.getSelectedValue();
        
        compromisso.notificacao.add(notificacao);
        
        ListModelNotificacao.clear();
        ListModelNotificacao.addAll(compromisso.getNotificacao());
        
        Listar();
        LimparCampos();
    }//GEN-LAST:event_SalvarNotificacaoActionPerformed

    private void jListCompromissosValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_jListCompromissosValueChanged
        Compromisso auxCompromisso = jListCompromissos.getSelectedValue();
        
        if(auxCompromisso != null){
            
            jTextFieldTitulo.setText(auxCompromisso.getTitulo());
            jTextAreaDescricao.setText(auxCompromisso.getDescricao());
            jFormattedTextFieldDataInicio.setText(auxDateFormat.format(auxCompromisso.getHoraInicio()));
            jFormattedTextFieldDataFim.setText(auxDateFormat.format(auxCompromisso.getHoraInicio()));
            
            ListModelNotificacao.clear();
            ListModelNotificacao.addAll(auxCompromisso.getNotificacao());
        }
        LimparNotificacao();
    }//GEN-LAST:event_jListCompromissosValueChanged

    private void LimparNotificacao(){
        
        jFormattedTextFieldTempo.setText("");
        jFormattedTextFieldUnidade.setText("");
        jFormattedTextFieldTipo.setText("");
    }
    
    
